-module('nine9s_app').

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    Dispatch = cowboy_router:compile(
                 [{'_', [
                         {"/", default_handler, []}
                        ]}
                 ]),
    {ok, _} = cowboy:start_http(http, 10, [{port, 9090}], 
                                [{env, [{dispatch, Dispatch}]}]),

    'nine9s_sup':start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

